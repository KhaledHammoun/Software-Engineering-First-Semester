public class Expressions
{
  public static void main(String[] args)
  {
    System.out.println(23 * 4.5 / 0.5 + 1);
    System.out.println(23 * 4.5 / (0.5 + 1));
    System.out.println(2 + 5 - 18 + 11);
    System.out.println((2 + 5) - (18 + 11));
    System.out.println(14 * 18 / 4 + 4);
    System.out.println(14 * 18 / (4 + 4));
  }
}
